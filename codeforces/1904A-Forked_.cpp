typedef vector< int > vi; typedef vector< vi > vvi;
typedef pair< int, int > pii; typedef vector< pii > vpii;
typedef vector< vpii > vvpii;
typedef long long ll; typedef vector< ll > vll;
typedef vector< vll > vvll; typedef pair< ll, ll > pll;
typedef vector< pll > vpll; typedef vector< vpll > vvpll;

template< typename T, typename T2 >
ostream& operator<<(ostream& os, const pair< T, T2 > &p) {
  os << "(" << p.first << "," << p.second << ")";
  return os;
}

int main() {
  ios_base::sync_with_stdio(false); 
  cin.tie(NULL);
  cout.setf(ios::fixed);
  cout.precision(4);
  int t = 1;
  cin >> t;
  while(t--) {
    int a, b;
    cin >> a >> b;
    int xk, yk;
    cin >> xk >> yk;
    int xq, yq;
    cin >> xq >> yq;

    vpii moves = {{-a,-b}, {-a, b}, {a, -b}, {a, b},
                  {-b, -a}, {-b, a}, {b, -a}, {b, a}}; 
    set< pii > king;
    set< pii > queen;
    for(auto move_ : moves) {
      int x = move_.first;
      int y = move_.second;
      king.insert({xk + x, yk + y});
      queen.insert({xq + x, yq + y});
    }
    set< pii > res;
    for(auto pos : queen) {
      if(king.find(pos) != king.end()) {
        res.insert(pos);
      }
    }
    cout << res.size() << "\n";
  }
  return 0;
}
